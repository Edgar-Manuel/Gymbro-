/**
 * Obtener conceptos por categoría
 */
export function getConceptosPorCategoria(categoria: ConceptoEducativo['categoria']): ConceptoEducativo[] {
  return conceptosEducativos.filter(c => c.categoria === categoria);
}

/**
 * Obtener conceptos críticos (los más importantes)
 */
export function getConceptosCriticos(): ConceptoEducativo[] {
  return conceptosEducativos.filter(c => c.importancia === 'critica');
}

/**
 * Buscar conceptos por término
 */
export function buscarConceptos(termino: string): ConceptoEducativo[] {
  const terminoLower = termino.toLowerCase();
  return conceptosEducativos.filter(c =>
    c.titulo.toLowerCase().includes(terminoLower) ||
    c.resumen.toLowerCase().includes(terminoLower) ||
    c.contenido.some(linea => linea.toLowerCase().includes(terminoLower))
  );
}
